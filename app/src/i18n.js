let _currentUiLang = 'en';

function i18n(key, ...args) {
  const strings = I18N_STRINGS[_currentUiLang] || I18N_STRINGS['en'];
  let msg = strings[key] || I18N_STRINGS['en'][key] || key;

  args.forEach((arg, i) => {
    msg = msg.replace(`$${i + 1}`, arg);
  });

  return msg;
}

function getLangData(code) {
  return I18N_STRINGS[code] || { flag: '', label: code };
}
